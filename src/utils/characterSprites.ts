// Color palette for the knight
export const knightColors = {
  1: '#8B4513', // Brown (armor/boots)
  2: '#C0C0C0', // Silver (helmet)
  3: '#FFD700'  // Gold (eyes/details)
};

// Function to render a sprite to canvas
export function renderSprite(
  ctx: CanvasRenderingContext2D,
  sprite: number[][],
  x: number,
  y: number,
  scale: number = 2,
  colors: Record<number, string> = knightColors
) {
  for (let row = 0; row < sprite.length; row++) {
    for (let col = 0; col < sprite[row].length; col++) {
      const pixel = sprite[row][col];
      if (pixel !== 0) {
        ctx.fillStyle = colors[pixel] || '#000000';
        ctx.fillRect(
          x + col * scale,
          y + row * scale,
          scale,
          scale
        );
      }
    }
  }
}

